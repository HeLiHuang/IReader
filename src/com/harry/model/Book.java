package com.harry.model;

public class Book {
	/**
	 * 文件路径
	 */
	private String path;
	/**
	 * 书名
	 */
	private String title;

	public String getPath() {
		return path;
	}

	public void setPath(String mPath) {
		path = mPath;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String mTitle) {
		title = mTitle;
	}
}
